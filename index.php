<h2>Sobre SKIP</h2>

<p>
    Nacido en 1990 en Vitoria-Gasteiz, DJ SKIP es actualmente uno de los DJs más famosos de Europa.
</p>
<p>
    Comenzó a interesarse por la música a los 10 años y a los 12 empezó su carrera como DJ.
    La mayoría de sus canciones están dentro de diferentes subgéneros de la música <strong>house</strong>,
    entre los que destacan el <strong>Electro House</strong> y  el <strong>Progressive House</strong>.
</p>
<p>
    Su primer éxito y su ascenso como DJ lo logró en 2007 cuando lanzó su primer álbum de la mano de la discográfica <em>Spinnin' records</em>.
    Desde entonces ha lanzado 3 álbumes más en 2009, 2012 y 2016 respectivamente.
</p>
<p>
    En 2011 tocó por primera vez en el festival belga <strong>Tomorrowland</strong>, habiendo repetido cada año. En 2012 se estrenó en el <strong>Ultra</strong> de Miami.
</p>

